#include "libft.h"

/**
 * @brief Frees a string array.
 * @param arr The string array to free.
 */
void	ft_free_arr(char **arr)
{
	int	i;

	i = 0;
	while (arr[i])
	{
		free(arr[i]);
		i++;
	}
	free(arr);
}
